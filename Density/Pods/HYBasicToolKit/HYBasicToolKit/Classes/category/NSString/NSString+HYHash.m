#pragma mark - 助手方法
/**
 *  返回二进制 Bytes 流的字符串表示形式
 *
 *  @param bytes  二进制 Bytes 数组
 *  @param length 数组长度
 *
 *  @return 字符串表示形式
 */
- (NSString *)hy_stringFromBytes:(uint8_t *)bytes length:(int)length {
    NSMutableString *strM = [NSMutableString string];
    
    for (int i = 0; i < length; i++) {
        [strM appendFormat:@"%02x", bytes[i]];
    }
    
    return [strM copy];
}
- (NSString *)hy_base64Encode:(NSString *)originalStr{
    //1.得把原始字符串转成二进制
    NSData *base64Data = [originalStr dataUsingEncoding:NSUTF8StringEncoding];
    
    
    //2.再将上面转好的二进制,编码成base64的字符串
    NSString *base64Str = [base64Data base64EncodedStringWithOptions:0];
    
    
    return base64Str;
}
//得到中英文混合字符串长度
- (int)hy_stringLenghConvert
{
    int strlength = 0;
    
    NSStringEncoding enc = CFStringConvertEncodingToNSStringEncoding(kCFStringEncodingGB_18030_2000);
    char* p = (char*)[self cStringUsingEncoding:enc];
    
    for (int i = 0 ; i < [self lengthOfBytesUsingEncoding:enc] ;i++) {
        if (*p) {
            p++;
            strlength++;
        }
        else {
            p++;
        }
    }
    return strlength;
}

